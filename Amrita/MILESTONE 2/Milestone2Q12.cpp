// TO PRINT ALL NATURAL NUMBERS TILL 'N'
#include<iostream>
using namespace std;
int main()
{
    int i,n;
    cout<<"UPTO HOW MUCH NUMBER DO YOU WANT TO PRINT THE NATURAL NUMBERS \n";
    cin>>n;
    cout<<"THE NUMBERS ARE \n";
    while(i<=n  )
    {
        cout<<i<<endl;
        i++;
    }
}
