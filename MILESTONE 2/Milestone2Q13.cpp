// TO PRINT ALL NATURAL NUMBERS FROM 'N' TO 1
#include<iostream>
using namespace std;
int main()
{
    int i=0,n;
    cout<<"UPTO HOW MUCH NUMBER DO YOU WANT TO PRINT THE NATURAL NUMBERS \n";
    cin>>n;
    cout<<"THE NUMBERS ARE \n";
    while(n>=i)
    {
        cout<<n<<endl;
        n--;
    }
}

