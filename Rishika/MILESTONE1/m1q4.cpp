#include<iostream>
using  namespace std;
int main()
{
	int n;
	cout<<"enter a number- ";
	cin>>n;
	if (n>0)
	{cout<<"the number is positive.";
	}
	if(n==0)
	{cout<<"the number is zero.";
	}
	if(n<0)
	{cout<<"the number is negative.";
	}return 0;
}
